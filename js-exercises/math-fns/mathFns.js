
// Don't change the export values.
// You can use the function in the Math module
function sqrt(number) {
  return Math.sqrt(number);
}

function power(number, toPower) {
  return number ** toPower;
}

function round(decimalNmber) {
  return Math.round(decimalNmber);
}

export {
  sqrt,
  power,
  round,
};
